import requests
import pandas as pd
import numpy as np
import math

# --- 1. CONFIGURATION & CONSTANTS ---
SOLAR_PEAK = 1073  # W/m^2 [cite: 307]
SOLAR_NOON_SEC = 12 * 3600  # 12:00 PM in seconds [cite: 307]
SIGMA = 11600  # Standard deviation in seconds [cite: 308]
PANEL_AREA = 4.6  # m^2 [cite: 309]
PANEL_EFFICIENCY = 0.21  # 21% [cite: 309]

def calculate_solar_irradiance(seconds_from_midnight):
    """Calculates incident solar irradiance using a Gaussian curve."""
    exponent = -((seconds_from_midnight - SOLAR_NOON_SEC)**2) / (2 * (SIGMA**2))
    irradiance = SOLAR_PEAK * math.exp(exponent)
    return irradiance

def get_elevation_data(lats, lons):
    """Fetches real elevation data from Open-Elevation API[cite: 300]."""
    url = "https://api.open-elevation.com/api/v1/lookup"
    payload = {
        "locations": [{"latitude": lat, "longitude": lon} for lat, lon in zip(lats, lons)]
    }
    try:
        response = requests.post(url, json=payload, timeout=20)
        if response.status_code == 200:
            return [res['elevation'] for res in response.json()['results']]
        return None
    except Exception as e:
        print(f"API Connection Error: {e}")
        return None

# 2. ROUTE GENERATION (Sasolburg to Zeerust)
# Coordinates for Sasolburg and Zeerust [cite: 285]
num_points = 100
lats = np.linspace(-26.81, -25.54, num_points)
lons = np.linspace(27.83, 26.08, num_points)

print("Fetching elevation data and building solar model...")
elevations = get_elevation_data(lats, lons)

if elevations:
    total_dist_m = 190000  # 190 km route
    dist_step = total_dist_m / (num_points - 1)

    race_times = np.linspace(8*3600, 17*3600, num_points)

    solar_values = [calculate_solar_irradiance(t) for t in race_times]
    power_gen = [val * PANEL_AREA * PANEL_EFFICIENCY for val in solar_values]

    # Create the Master DataFrame [cite: 304]
    route_df = pd.DataFrame({
        'timestamp_sec': race_times,
        'latitude': lats,
        'longitude': lons,
        'altitude': elevations,
        'irradiance_wm2': solar_values,
        'potential_solar_power_w': power_gen
    })

    # Calculate Slope (Rise over Run)
    route_df['alt_diff'] = route_df['altitude'].diff().fillna(0)
    route_df['slope_rad'] = np.arctan2(route_df['alt_diff'], dist_step)

    # --- 4. EXPORT ---
    route_df.to_csv("route_environment.csv", index=False)
    print("Phase 1 Complete: 'route_environment.csv' generated with real terrain data.")
else:
    print("Critical Error: Elevation data retrieval failed.")

